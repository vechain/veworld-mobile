import CryptoUtils from "./index"

describe("random number tests", () => {
    test("random number should be between 0 and 1", () => {
        for (let i = 0; i < 10; i++) {
            const random = CryptoUtils.random()
            expect(random).toBeGreaterThanOrEqual(0)
            expect(random).toBeLessThanOrEqual(1)
        }
    })
})
