const compareURLs = (url1?: string, url2?: string) => {
    if (!url1 || !url2) return false

    const parsedURL1 = new URL(url1)
    const parsedURL2 = new URL(url2)
    return (
        parsedURL1.origin === parsedURL2.origin &&
        parsedURL1.pathname === parsedURL2.pathname
    )
}

export default {
    compareURLs,
}
